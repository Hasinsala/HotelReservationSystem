public class doubleRoom {
    private int doubleTotal;
    private double doublePricePerRoom;

    public doubleRoom(int Total,double pricePerRoom){
        this.doubleTotal=Total;
        this.doublePricePerRoom=pricePerRoom;

    }
    public int getDoubleTotal(){
        return doubleTotal;
    }
    public double getDoublePricePerRoom(){
        return doublePricePerRoom;
    }

}
